package tetrisgame;

/**
 * Interface that listens for when a game is started
 * or ended.
 * 
 * @author Alex Stringham
 * @version Nov. 2011
 */
public interface GameListener
{
  /**
   * Called every time a game starts or ends.
   * 
   * @param the_event a GameEvent
   */
  void gameChange(final GameEvent the_event);
}
